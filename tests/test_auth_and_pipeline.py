def test_github_dashboard_with_badges(monkeypatch):
    def fake_build_dashboard(self, repository=None, branch=None):
        return GitHubDashboardResponse(
            repository=repository or 'octocat/Hello-World',
            default_branch=branch or 'main',
            health_score=91,
            build_success_rate=95.5,
            deployments_this_week=2,
            open_issues=10,
            stars=20,
            pull_requests_open=3,
            pipeline_stages=[StageMetric(stage='Build', status='success', duration=22)],
            commits=[CommitItem(sha='1234567', message='Init', author='octocat', time='1h ago')],
            workflow_runs=[WorkflowRun(name='CI', branch='main', status='completed', conclusion='success', duration_seconds=22, started_at='2026-04-20T00:00:00Z', workflow_name='CI Pipeline', workflow_file='ci.yml')],
            badges=[BadgeItem(label='CI Pipeline', image_url='https://example.com/badge.svg', target_url='https://example.com/workflow')],
            integration_status='live',
        )

    monkeypatch.setattr(GitHubService, 'build_dashboard', fake_build_dashboard)

    with TestClient(app) as client:
        login_response = client.post('/api/auth/login', data={'username': 'admin@example.com', 'password': 'Admin123!'})
        token = login_response.json()['access_token']
        response = client.get('/api/github/dashboard?repository=octocat/Hello-World', headers={'Authorization': f'Bearer {token}'})
        assert response.status_code == 200
        payload = response.json()
        assert payload['repository'] == 'octocat/Hello-World'
        assert payload['integration_status'] == 'live'
        assert payload['badges'][0]['label'] == 'CI Pipeline'
